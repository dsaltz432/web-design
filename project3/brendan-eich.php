	<h2>Brendan Eich</h2>

	<p>Brendan Eich is an American technologist and creator of the JavaScript programming language. He co-founded the Mozilla project, the Mozilla Foundation and the Mozilla Corporation, and served as the Mozilla Corporation's chief technical officer and briefly its chief executive officer.</p>

	<p>Brendan Eich received his bachelor's degree in mathematics and computer science at Santa Clara University. He received his master's degree in 1985 from the University of Illinois at Urbana-Champaign.
	Eich started his career at Silicon Graphics, working for seven years on operating system and network code. He then worked for five years at MicroUnity Systems Engineering writing microkernel and DSP code, and doing the very first MIPS R4000 port of GCC.
	</p>

	<p> He started work at Netscape Communications Corporation in April 1995. Having originally joined intending to put Scheme "in the browser", Eich was instead commissioned to create a new language that resembled Java, JavaScript for the Netscape Navigator Web browser. The first version was completed in ten days in order to accommodate the Navigator 2.0 Beta release schedule, and was called Mocha, which was later renamed LiveScript in September 1995 and later JavaScript in the same month. Eich continued to oversee the development of SpiderMonkey, the specific implementation of JavaScript in Navigator, until 2011.</p>

	<p> In early 1998, he helped found the Mozilla project, with a website at mozilla.org, which was meant to manage open-source contributions to the Netscape source code. He served as Mozilla's chief architect. AOL bought Netscape in 1999. After AOL shut down the Netscape browser unit in July 2003, Eich helped spin out the Mozilla Foundation.
	In August 2005, after serving as Lead Technologist and as a member of the Board of Directors of the Mozilla Foundation, Eich became CTO of the newly founded Mozilla Corporation, meant to be the Mozilla Foundation's for-profit arm.
	</p>

	<p> On March 24, 2014, Eich was promoted to CEO of Mozilla Corporation. His appointment sparked controversy over a $1,000 political donation Eich had made in 2008 to the successful campaign for California Proposition 8, which sought to establish that, "Only marriage between a man and a woman is valid or recognized in California."  This had been noted publicly two years prior, in March 2012, and was criticized at the time in social media by gay rights activists, particularly on Twitter. After his appointment to CEO, the controversy reemerged. In the ensuing public debate, OKCupid and two gay application developers called for a boycott of the company. A number of Mozilla Foundation employees asked him to step down, while others at the Mozilla Corporation spoke out on their blogs in his favor. Three of Mozilla Corporation's six directors resigned just prior to Eich's appointment, which the Mozilla Foundation attributed to "a variety of reasons. Two of the board members had been planning to leave for some time, one since January and one explicitly at the end of the CEO search, regardless of the person selected. “On April 3, 2014, Eich stepped down as CEO and resigned from working at Mozilla. In his personal blog, Eich posted that "under the present circumstances, I cannot be an effective leader." Following Eich's resignation, the National Organization for Marriage called for its own boycott of Mozilla, due to his resigning instead of explaining his own view. </p>
