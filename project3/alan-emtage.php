				<h2> Career </h2>
					
					<h3> Archie </h3>
						<p> In 1989 while a student and working as a systems administrator for the School of Computer Science, Alan conceived and implemented the original version of the Archie search engine, the world's first Internet search engine and the start of a line which leads directly to today's giants Yahoo and Google. (The name Archie stands for "archives" without the "v", not the kid from the comics)
						Working as a systems administrator, Alan was responsible for locating software for the students and staff of the faculty. The necessity for searching information became the mother of invention.
						He decided to develop a set of programs, that would go out and look through the repositories of software (public anonymous FTP sites) and build basically an index of the available software, a searchable database of filename. One thing led to another and word got out that he had an index available and people started writing in and asking if we could search the index on their behalf.
						As a result rather than having doing it himself, he allowed them to do it themselves so we wrote software that would allow them to come in and search the index themselves. That was the beginning.
						It seems that the administration of the university was the last to find out about what Alan had done. As Alan remembered: "We had no permission from the school to provide this service; and as a matter of fact, the head or our department found out about it for the first time by going to a conference. Somebody went up to him and said they really wanted to congratulate him for providing this service and he graciously smiled, said 'You're welcome' and went back to McGill and said 'what the hell is all of this? I have no idea what they're talking about'."
						"That was a once in a lifetime opportunity. It was largely being in the right place at the right time with the right idea. There were other people who had similar ideas and were working on similar projects, I just happened to get there first."
						Archie is considered the original search engine and a lot of the techniques that Emtage and other people that worked with him on Archie came up with are basically the same techniques that Google, Yahoo! and all the other search engines use.
						Later Alan and his colleagues developed various versions that allowed them to split up the service so that it would be available at other universities rather than taxing the facility at McGill.
						</p>

