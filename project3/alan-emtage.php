					<h3> Building on Archie </h3>
						<p> In 1992, Emtage along with Peter J. Deutsch formed Bunyip Information Systems the world's first company expressly founded for and dedicated to providing Internet information services with a licensed commercial version of the Archie search engine.  Later, he became a member of the Internet Society, an international, non-profit organization founded in 1992 to provide leadership in Internet related standards, education, and policy.
						Emtage was a founding member of the Internet Society and went on to create and chair several working groups at the Internet Engineering Task Force (IETF), the standard-setting body for the Internet. Working with other pioneers such as Tim Berners-Lee, Marc Andreessen, Mark McCahill (creator of Gopher) and Jon Postel, Emtage co-chaired the Uniform Resource Identifier working group which created the standard for Uniform Resource Locators (URLs).
						</p>
					
