		<h3>Wealth and Personal Life</h3>
		<p>In February 2012, Forbes estimated Dell's net worth at $15.9 billion. Dell married Susan Lynn Lieberman on October 28, 1989 in Austin, Texas; the couple reside there with their four children.</p>

		<h3>Philanthropy</h3>
		<ol>

		<li>In 1999, Michael and Susan Dell established the Michael and Susan Dell Foundation, which focuses on, among other causes, grants, urban education, childhood health and family economic stability. The foundation has provided $65 million in grants to three health-related organizations associated with the University of Texas: the Michael and Susan Dell Center for Advancement of Healthy Living, the Dell Pediatric Research Institute, and the Dell Children's Medical Center, as well as funding for a new computer science building at the University of Texas at Austin campus.</li>

		<li>By 2010, the foundation had committed more than $650 million to children's issues and community initiatives in the United States, India and South Africa Today the foundation has over $466 million assets under management.</li>

		<li>In 2002, Dell received an honorary doctorate in Economic Science from the University of Limerick in honor of his investment in Ireland and the local community and for his support for educational initiatives.</li>

		<li>In 2012, the Michael and Susan Dell Foundation committed $50 million for medical education. The Dell School of Medicine at the University of Texas at Austin will begin enrolling students in 2016.</li>

		<li>In 2014, he donated $1.8 million to the Friends of the Israel Defense Forces.</li>

		</ol>

		<h3>Criticsm</h3>
		<p>In the April 2011 issue of Mother Jones, a timeline of Michael Dell's life is detailed in "American Magnate: Michael Dell: How a homegrown geek outsourced, downsized, and tax-breaked his way to the top." The article juxtaposes Dell's spending on luxurious homes and private jet travel with his pursuit of tax breaks and tax holidays and Dell Computer's eventual offshoring of jobs overseas after receiving the incentives for setting up shop locally.</p>
