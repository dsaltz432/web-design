		<h2>Background</h2>
		<p>Michael Saul Dell (born February 23, 1965) is an American business magnate, investor, philanthropist, and author. He is the founder and CEO of Dell Inc., one of the world’s leading sellers of personal computers (PCs). He was ranked the 41st richest person in the world on 2012 Forbes list of billionaires, with a net worth of US$22.4 billion as of December 2014.</p>

		<p>In 2011, his 243.35 million shares of Dell stock were worth $3.5 billion, giving him 12% ownership of the company. His remaining wealth of roughly $10 billion is invested in other companies and is managed by a firm whose name, MSD Capital, incorporates Dell's initials. On January 5, 2013 it was announced that Michael Dell had bid to take Dell Inc. private for $24.4 billion in the biggest leveraged buyout since the Great Recession.</p>

		<h2>Early Life</h2>
		<p>Michael Dell was born in 1965 in Houston, to a Jewish family whose surname reflects the translation into English of the original German/Yiddish Thal ("valley" or "dale [q.v.]"; modern common-noun spelling Tal) upon the family's immigration to the United States. The son of Lorraine Charlotte (née Langfan), a stockbroker, and Alexander Dell, an orthodontist, Michael Dell attended Herod Elementary School in Houston. In a bid to enter business early, he applied to take a high school equivalency exam at age eight. In his early teens, he invested his earnings from part-time jobs in stocks and precious metals.</p>

		<p>Dell purchased his first calculator at age seven and encountered an early teletype terminal in junior high. At age 15, after playing with computers at Radio Shack, he got his first computer, an Apple II, which he promptly disassembled to see how it worked. Dell attended Memorial High School in Houston, selling subscriptions to the Houston Post in the summer. While making cold calls, he noted that the persons most likely to purchase subscriptions were those in the process of establishing permanent geographic and social presence; he then targeted this demographic group by collecting names from marriage and mortgage applications. Dell earned $18,000 that year, exceeding the annual income of his history and economics teacher.</p>

		<h2>Business Career</h2>
		<p>While a freshman pre-med student at the University of Texas, Dell started an informal business putting together and selling upgrade kits for personal computers in Room 2713 of the Dobie Center residential building. He then applied for a vendor license to bid on contracts for the State of Texas, winning bids by not having the overhead of a computer store.</p>

		<p>In January 1984, Dell banked on his conviction that the potential cost savings of a manufacturer selling PCs directly had enormous advantages over the conventional indirect retail channel. In January 1984, Dell registered his company as "PC's Limited". Operating out of a condominium, the business sold between $50,000 and $80,000 in upgraded PCs, kits, and add-on components. In May, Dell incorporated the company as "Dell Computer Corporation" and relocated it to a business center in North Austin. The company employed a few order takers, a few more people to fulfill them, and, as Dell recalled, a manufacturing staff "consisting of three guys with screwdrivers sitting at six-foot tables". The venture's capitalization cost was $1,000.</p>

		<p>In 1992, aged 27, he became the youngest CEO of a company ranked in Fortune magazine's list of the top 500 corporations. In 1996, Dell started selling computers over the Web, the same year his company launched its first servers. Dell Inc. soon reported about $1 million in sales per day from dell.com. In the first quarter of 2001, Dell Inc. reached a world market share of 12.8 percent, passing Compaq to become the world's largest PC maker. The metric marked the first time the rankings had shifted over the previous seven years. The company's combined shipments of desktops, notebooks and servers grew 34.3 percent worldwide and 30.7 percent in the United States at a time when competitors' sales were shrinking.</p>

		<p>In 1998, Dell founded MSD Capital L.P. to manage his family's investments. Investment activities include publicly traded securities, private equity activities, and real estate. The firm employs 80 people and has offices in New York, Santa Monica and London. Dell himself is not involved in day-to-day operations. On March 4, 2004, Dell stepped down as CEO, but stayed as chairman of Dell Inc.'s board, while Kevin Rollins, then president and COO, became president and CEO. On January 31, 2007, Dell returned as CEO at the request of the board, succeeding Rollins.</p>
	
