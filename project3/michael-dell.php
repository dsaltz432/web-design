		<h2>Penalty</h2>
		<p>In July 2010 Dell Inc. agreed to pay a $100 million penalty to settle SEC charges of disclosure and accounting fraud in relation to undisclosed payments from Intel Corporation. Michael Dell and former CEO Kevin Rollins agreed to pay $4 million each, former CFO James Schneider to pay $3 million to settle the charges.</p>

		<h2>Accolades</h2>
		<p>Accolades for Dell include "Entrepreneur of the Year" (at age 24) from Inc. magazine; "Top CEO in American Business" from Worth magazine; "CEO of the Year" from Financial World, Industry Week and Chief Executive magazines. Dell also received the 2013 Franklin Institute's Bower Award for Business Leadership.</p>

		<h2>Affiliations</h2>
		<p>Dell serves on the Foundation Board of the World Economic Forum, the executive committee of the International Business Council, the U.S. Business Council, and the governing board of the Indian School of Business in Hyderabad, India. He previously served as a member of the U.S. President's Council of Advisors on Science and Technology</p>

		<h2>Writings</h2>
		<p>Dell's 1999 book, Direct from Dell: Strategies That Revolutionized an Industry, is an account of his early life, his company's founding, growth and missteps, as well as lessons learned. The book was written in collaboration with Catherine Fredman.</p>

